describe('Twitter config', function() {
  it('should define twitter object');
  it('should define twitter.username property');
  it('should access non-empty env.ICOC_TWITTER_USERNAME variable');
  it('should assign twitter.username to be equal to env.ICOC_TWITTER_USERNAME');
  it('should define twitter.consumerKey property');
  it('should access non-empty env.ICOC_TWITTER_CONSUMER_KEY variable');
  it('should assign twitter.consumerKey to be equal to env.ICOC_TWITTER_CONSUMER_KEY');
  it('should define twitter.consumerSecret property');
  it('should access non-empty env.ICOC_TWITTER_CONSUMER_SECRET variable');
  it('should assign twitter.consumerSecret to be equal to env.ICOC_TWITTER_CONSUMER_SECRET');
  it('should define twitter.accessToken property');
  it('should access non-empty env.ICOC_TWITTER_ACCESS_TOKEN variable');
  it('should assign twitter.accessToken to be equal to env.ICOC_TWITTER_ACCESS_TOKEN');
  it('should define twitter.accessTokenSecret property');
  it('should access non-empty env.ICOC_TWITTER_ACCESS_TOKEN_SECRET variable');
  it('should assign twitter.accessTokenSecret to be equal to env.ICOC_TWITTER_ACCESS_TOKEN_SECRET');
});